      <h4>Day 13 - Sunday 24/09/17</h4>
      Waking up at the base of Humbug Mountain I pack up the tent and hit the 101 toward Portland. To make up some time I take the 38 across to the I-5 make the journey a bit quicker.
      It's not long before I arrive in Portland. I heard from other travels and TV shows it's a city full of hipsters. I feel like that is true. The Saturday market (also open Sundays)
       is full of quirky pieces of art and posh fast food. Food trucks have taken up permanent residency in a parking lot a short walk from the micro brewery block. Among all this locals
      can be seen cart literal carts full of used books to sell at Powell's book store. After an evening of excellent craft beer and excellent bluegrass music by Kendl Winter at Al's Den I retire to the hostel. 
