   <h2>Films</h2>
   Love a good film. I will give anything a chance but I maily watch sci-fi/thrillers. 
   
   <h3>The list (no particular order, I like them all)</h3>
   <ul>

      <li>Primer <br>
      Great film by <a href="http://en.wikipedia.org/wiki/Shane_Carruth" target="_Blank">Shane Carruth</a> about a group engineers that accidentally build a time machine.
      At least I think that's what happens.
      It's really quite hard to follow but that's why I like it so much.

      <li>2001: A Space Odyssey<br>
      A must for anyone in to sci-fi or <a href ="http://en.wikipedia.org/wiki/Stanley_Kubrick" target="_Blank">Stanley Kubrick</a> films.
      The long sweeping shots of the space craft hold there own against any modern film and I think always will.
      Then there's the mystery behind the monolith and it's effect on mankind.
      Co-written with <a href="http://en.wikipedia.org/wiki/Arthur_C._Clarke" target="_Blank">Arthur C. Clarke</a> but I read he didn't enjoy the film.

