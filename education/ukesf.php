	<h3>UKESF Scholar of the Year 2013</h3>
	Submitted report: <a href="../AshleyRobinsonUKESF.pdf" target="_blank">here</a> 
	<br>
	Press release: <a href="http://www.ukesf.org/news/articles/2013-ukesf-scholar-of-year" target="_blank">http://www.ukesf.org/news/articles/2013-ukesf-scholar-of-year</a>
	<p>	
	November of 2013, I was award scholar of the year by UKESF! 
	This was for my work towards developing LCD controller and NFC chips.
	Promoting electronics to A-level students was also praised by the award.	
	</p>
	<p>
	The scholarship has been a great addition to my academic life, and my experience with CSR and the UKESF professional development workshop will prove invaluable as I start my career when I graduate next year. Through the scholarship I've been able to connect with industry and with other like-minded engineers, so I appreciate the level of competition I was up against to get this award, it's a great honour. 
	</p>
	<p>
	Neil Dickins, director of the recruitment agency IC Group,the award's sponsor said: "All four finalists made valuable contributions to their sponsoring companies and in encouraging school students to pursue studies in STEM and electronic engineering. Ultimately a difficult task, we decided that Ashley should take the award as he produced a particularly professional looking submission demonstrating excellent communication skills. The industry welcomes well rounded individuals like Ashley and we wish him the best in his career."
	</p>
