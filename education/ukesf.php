   <h2>UK Electronics Skill Foundation</h2>

   Please visit <a href="http://www.ukesf.org/" target="_blank">www.ukesf.org</a> to find everything you need to know about the scholarship scheme.

   <p>
   I have been involved in the UKESF scheme since the second semester of my second year of university.
   Partnered with <a href="../work_experience/csr.php" target="content">CSR</a> I spent my summers working in their digital design department.
   Apart from the generous bursary provided the scholarship gave me a great chance to work on skills which prospective employers seek.
   A workshop is mandatory which brings together scholars to share their experiences and hone skills that promote teamwork.
   It was also a great opportunity to meet students from all over the UK who share the same interests in engineering as myself.
   </p>
